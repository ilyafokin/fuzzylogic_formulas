function meanW(parts::Vector)
    ret = 0
    Ws = [[] for part in parts]
    for (idx, part) in enumerate(parts)
        if part == "p"
            Ws[idx] = Ws_p
        elseif part == "pi"
            Ws[idx] = Ws_pi
        elseif part == "bg"
            Ws[idx] = Ws_bg
        else
            throw(ArgumentError("Unknown particle type $a"))
        end
    end

    for w in zip(Ws...)
        ret += *(w...)
    end

    ret / length(first(Ws))
end

function meanW(part::String)
    meanW([part])
end

function meanW(part1::String, part2::String)
    meanW([part1, part2])
end

function meanW(part1::String, part2::String, part3::String)
    meanW([part1, part2, part3])
end

function meanW(part1::String, part2::String, part3::String, part4::String)
    meanW([part1, part2, part3, part4])
end

